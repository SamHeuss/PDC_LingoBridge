    private void enterSuggestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterSuggestActionPerformed
        // TODO add your handling code here:
        String username = checkUser.getText().trim();
    String suggestion = inputSuggest.getText().trim();

    // Check if the username and suggestion are not empty
    if (!username.isEmpty() && !suggestion.isEmpty()) {
        // Get the table's model
        DefaultTableModel model = (DefaultTableModel) suggestedTable.getModel();

        // Add a new row with the entered values
        model.addRow(new Object[]{username, suggestion});
        dbmanager.insertSuggestion(username, suggestion);


        // Optionally clear the text fields
        checkUser.setText("");
        inputSuggest.setText("");
        repaint();
    } else {
        JOptionPane.showMessageDialog(this, "Both username and suggestion must be entered", "Warning", JOptionPane.WARNING_MESSAGE);
    }

        
    }//GEN-LAST:event_enterSuggestActionPerformed

