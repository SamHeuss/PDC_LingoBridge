    private void checkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkButtonActionPerformed
        // TODO add your handling code here:
        String username = checkUser.getText().trim();
        if (isUserValid(username)) {
            enterSuggest.setEnabled(true); // Enable the 'Enter' button if the user is valid
        } else {
            JOptionPane.showMessageDialog(this, "Enter a valid username", "Warning", JOptionPane.WARNING_MESSAGE);
            enterSuggest.setEnabled(false); // Keep the 'Enter' button disabled if the user is invalid
        }
    }//GEN-LAST:event_checkButtonActionPerformed

    private boolean isUserValid(String username) {
        return dbmanager.usernameExists(username);
    }

