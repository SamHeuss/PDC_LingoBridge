    private void confirmButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                               
        int selectedDeck = jComboBox1.getSelectedIndex(); // 0-based index
        if (selectedDeck != 0) {
            handleDeckSelection(selectedDeck);
        } else {
            JOptionPane.showMessageDialog(DeckSelection.this, "Please select a valid deck.");
        }
    }                                              

