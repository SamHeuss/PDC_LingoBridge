    private void handleDeckSelection(int deck) {
    selectedDeckValue = decks[deck];
    
    // Displaying a confirmation dialog with "OK" and "Cancel" options
    int response = JOptionPane.showConfirmDialog(this, 
                                                "You selected deck: " + selectedDeckValue + ". Proceed?",
                                                "Confirm Selection",
                                                JOptionPane.OK_CANCEL_OPTION,
                                                JOptionPane.QUESTION_MESSAGE);
    
    // If the user clicks on "OK"
    if (response == JOptionPane.OK_OPTION) {
        firePropertyChange("deckSelected", false, true);
    } 
    // If the user clicks on "Cancel"
    else if (response == JOptionPane.CANCEL_OPTION) {
        // Here you can reset the combo box or navigate back to the deck selection, based on your requirements
        jComboBox1.setSelectedIndex(0); // Setting the combo box back to the default option
    }
}
    
    
